    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            
            int devine = Integer.parseInt(comb.getText());
            
           int cpt = comb.getText().length();
            if (cpt != 4)
            {
                JOptionPane.showMessageDialog(this, "erreur");
            }
            else {
            //prendre le jeu courrant , deviner et afficher le resultat
            partie p = jeu.getCurrentGame();
            String res = jeu.deviner(p, devine);
            if(res.equals("+ + + + "))
            {
               
                JOptionPane.showMessageDialog(this, "Game over,restart");
                rest.setEnabled(true);
            }
         
            resultat.setText(res);
             
            }
        } catch (RemoteException ex) {
            Logger.getLogger(MasterMind.class.getName()).log(Level.SEVERE, null, ex);
        }
 
       
    }//GEN-LAST:event_jButton1ActionPerformed

